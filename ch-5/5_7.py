# Exercise 5-7. Favorite Fruit: Make a list of your favorite fruits, and then
# write a series of independent if statements that check for certain fruits in
# your list.
"""
• Make a list of your three favorite fruits and call it favorite_fruits.
• Write five if statements. Each should check whether a certain kind of fruit 
is in your list. If the fruit is in your list, the if block should print a 
statement, such as You really like bananas!
"""

favorite_fruits = ["bananas", "oranges", "avocados"]

if "bananas" in favorite_fruits:
    print(f"You really like bananas!")

if "oranges" in favorite_fruits:
    print(f"You really like oranges!")

if "avocados" in favorite_fruits:
    print(f"You really like avocados!")

if "strawberry" in favorite_fruits:
    print(f"You really like strawberry!")

if "apples" in favorite_fruits:
    print(f"You really like apples!")
